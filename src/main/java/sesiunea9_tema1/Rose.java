package sesiunea9_tema1;

public class Rose extends Orange {
    String roseColor;
    String roseType;
    int rosePrice;

    public String getRoseColor() {
        return roseColor;
    }

    public void setRoseColor(String roseColor) {
        this.roseColor = roseColor;
    }

    public String getRoseType() {
        return roseType;
    }

    public void setRoseType(String roseType) {
        this.roseType = roseType;
    }

    public int getRosePrice() {
        return rosePrice;
    }

    public void setRosePrice(int rosePrice) {
        this.rosePrice = rosePrice;
    }
}
